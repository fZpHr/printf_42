#include "ft_printf.h"

void	ft_putstr_fd(char *str, int fd)
{
	int		i;

	i = 0;
	if (!str)
		return ;
	while (str[i] != '\0')
	{
		write(fd, &str[i], 1);
		i++;
	}
}
